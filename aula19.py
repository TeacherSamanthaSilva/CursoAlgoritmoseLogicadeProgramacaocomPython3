# Exercicio 15
# Crie duas variáveis que vão receber números, faça a conversão delas e exiba a soma desses dois números

primeironumero = int(input("Digite o primeiro número: "))
segundonumero = int(input("Digite o segundo número: "))
soma = primeironumero + segundonumero

print("A soma do %i e do %i é %i" % (primeironumero, segundonumero, soma))
